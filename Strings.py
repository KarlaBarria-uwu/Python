#INDEXACIÓN: Accede a un carácter por su índice.
palabra = "Antonia"
print(palabra[0]) #accede a la primera letra
print(palabra[-2]) #accede a la penúltima letra

#SUBSTRINGS: Obtiene una parte del string indicando el inicio y terminoe captura
texto = "Llanos Juan"
print(texto[7:11]) #obtiene la palabra Juan